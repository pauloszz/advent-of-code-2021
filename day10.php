$invalidCharacters = [];
$syntax_error_score = 0;
$legal_pairs = ['()', '[]', '{}', '<>'];
$legal_pair_opening = ['(', '[', '{', '<'];
$legal_pair_closing = [')', ']', '}', '>'];
$illegal_character_scores = [
	')' => 3,
	']' => 57,
	'}' => 1197,
	'>' => 25137,
];
$count_illegal_character_occured = [
	')' => 0,
	']' => 0,
	'}' => 0,
	'>' => 0,
];

foreach ($lines as $line => $input) {
	$characters = str_split($input);
	$stack = [];
	foreach ($characters as $character) {
		if (count($stack) === 0 && in_array($character, $legal_pair_closing)) {
			$count_illegal_character_occured[$character]++;
			unset($lines[$line]);
			break;
		}
		if (in_array($character, $legal_pair_opening)) {
			$stack[] = $character;
			continue;
		}
		$pair = end($stack) . $character;
		if (in_array($pair, $legal_pairs)) {
			array_pop($stack);
			continue;
		}
		$count_illegal_character_occured[$character]++;
		unset($lines[$line]);
		break;
	}
	unset($stack);
}

foreach ($count_illegal_character_occured as $character => $count) {
	$syntax_error_score += ($count * $illegal_character_scores[$character]);
}

echo "Syntax error score: " . $syntax_error_score;

echo "<h3>Part 2</h3>";

$legal_pairs_array = ['(' => ')', '[' => ']', '{' => '}', '<' => '>'];
$legal_closing_character_scores = [
	')' => 1,
	']' => 2,
	'}' => 3,
	'>' => 4,
];
$points = [];

foreach ($lines as $line => $input) {
	$characters = str_split($input);
	$stack = [];
	foreach ($characters as $character) {
		if (in_array($character, $legal_pair_opening)) {
			$stack[] = $character;
			continue;
		}
		$pair = end($stack) . $character;
		if (in_array($pair, $legal_pairs)) {
			array_pop($stack);
			continue;
		}
	}
	$incomplete_characters = count($stack);
	if ((bool) $incomplete_characters) {
		// echo "\t Adding missing characters: " . implode('', $stack) . ' :: ';
		$character_added = [];
		for ($i = 0; $i < $incomplete_characters; $i++) {
			$character = array_pop($stack);
			$character_added[] = $legal_pairs_array[$character];
		}
		// echo implode('', $character_added);
		$score = 0;
		foreach ($character_added as $character) {
			$score = ($score * 5) + $legal_closing_character_scores[$character];
		}
		// echo " - $score<br>";
		$points[] = $score;
	}
	unset($stack);
}

sort($points);

$middle_score = $points[(count($points) - 1) / 2];

echo "Middle score: " . $middle_score;
